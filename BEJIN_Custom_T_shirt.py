        "    order_time = now.strftime(\"%H:%M:%S\")\n",
        "\n",
        "    # Outer Border with Brand Color #2F3E2B (Dark Olive Green)\n",
        "    draw.rectangle([40, 40, 960, receipt_height - 40], outline=(47, 62, 43), width=3)\n",
        "\n",
        "    # Header Branding Text\n",
