        "# --- Helper function to fetch open-source emoji PNGs safely over HTTP ---\n",
        "@st.cache_data(show_spinner=False)\n",
        "def fetch_emoji_image(emoji_name):\n",
        "    emoji_urls = {\n",
        "        \"Cute Bear 🧸\": \"https://cdnjs.cloudflare.com/ajax/libs/twemoji/14.0.2/72x72/1f43b.png\",\n",
        "        \"Pink Bow 🎀\": \"https://cdnjs.cloudflare.com/ajax/libs/twemoji/14.0.2/72x72/1f380.png\",\n",
