        "    if item['f_emoji'] != \"None\": price += 2000\n",
        "    if item['f_img'] is not None: price += 5000\n",
        "    if item['b_emoji'] != \"None\": price += 2000\n",
        "    if item['b_img'] is not None: price += 5000\n",
        "    return price\n",
        "\n",
        "# --- Receipt Engine Updated to matching plm. studio color tones ---\n",
