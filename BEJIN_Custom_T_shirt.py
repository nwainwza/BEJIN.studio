        "        \"Retro Star 🌟\": \"https://cdnjs.cloudflare.com/ajax/libs/twemoji/14.0.2/72x72/1f31f.png\",\n",
        "        \"Fresh Clover 🍀\": \"https://cdnjs.cloudflare.com/ajax/libs/twemoji/14.0.2/72x72/1f340.png\",\n",
        "        \"Cherries 🍒\": \"https://cdnjs.cloudflare.com/ajax/libs/twemoji/14.0.2/72x72/1f352.png\",\n",
