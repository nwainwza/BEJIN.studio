        "    }\n",
        "    url = emoji_urls.get(emoji_name)\n",
        "    if url:\n",
        "        try:\n",
        "            req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0'})\n",
        "            with urllib.request.urlopen(req, timeout=3) as response:\n",
        "                return Image.open(io.BytesIO(response.read())).convert(\"RGBA\")\n",
        "        except:\n",
        "            return None\n",
        "    return None\n",
        "\n",
        "# --- Mockup Engine (Accepts dynamic Text Size and Image Size parameters) ---\n",
        "def create_shirt_mockup(color_name, text, text_size, uploaded_file, img_size, emoji, pos=\"Center\"):\n",
        "    color_map = {\n",
        "        \"Classic White ⚪\": (255, 255, 255),\n",
        "        \"Pitch Black ⚫\": (30, 30, 30),\n",
        "        \"Navy Blue 🔵\": (20, 40, 80),\n",
        "        \"Deep Green 🟢\": (20, 60, 40)\n",
        "    }\n",
        "    bg_color = color_map.get(color_name, (255, 255, 255))\n",
        "    text_color = (255, 255, 255) if color_name not in [\"Classic White ⚪\"] else (50, 50, 50)\n",
        "\n",
        "    img = Image.new('RGB', (400, 400), (243, 242, 238)) # Aesthetic light grid background tint\n",
        "    draw = ImageDraw.Draw(img)\n",
        "\n",
        "    # Neckline\n",
        "    draw.ellipse([160, 30, 240, 70], fill=(225, 225, 225), outline=(210, 210, 210))\n",
        "\n",
        "    # Balanced Silhouette\n",
        "    shirt_points = [\n",
        "        (160, 50), (240, 50),      # Collar\n",
        "        (330, 90), (350, 150),     # Right Sleeve\n",
        "        (290, 150), (280, 360),    # Right Torso\n",
        "        (120, 360), (110, 150),    # Left Torso\n",
        "        (110, 150), (50, 150),     # Left Sleeve\n",
        "        (70, 90)                   # Left Shoulder\n",
        "    ]\n",
        "    draw.polygon(shirt_points, fill=bg_color, outline=(200, 200, 200), width=1)\n",
        "    draw.line([(120, 360), (280, 360)], fill=(210, 210, 210), width=2)\n",
        "\n",
        "    # Horizontal Alignment Logic\n",
        "    x_pos = 200\n",
        "    if pos == \"Left\": x_pos = 145\n",
        "    elif pos == \"Right\": x_pos = 255\n",
        "\n",
        "    # Vertical Layering (Emoji @ 105, Text @ 125, Logo @ 160)\n",
        "    if emoji and emoji != \"None\":\n",
        "        e_img = fetch_emoji_image(emoji)\n",
        "        if e_img:\n",
        "            try:\n",
        "                e_img.thumbnail((26, 26))\n",
        "                img.paste(e_img, (x_pos - e_img.width//2, 105 - e_img.height//2), e_img)\n",
        "            except: pass\n",
        "\n",
        "    if text:\n",
        "        draw.text((x_pos, 125), text, fill=text_color, anchor=\"mm\", font_size=int(text_size))\n",
        "\n",
        "    if uploaded_file:\n",
        "        try:\n",
        "            file_bytes = uploaded_file if isinstance(uploaded_file, bytes) else uploaded_file.read()\n",
        "            u_img = Image.open(io.BytesIO(file_bytes)).convert(\"RGBA\")\n",
        "            u_img.thumbnail((int(img_size), int(img_size))) # Modified to use dynamic img_size\n",
        "            img.paste(u_img, (x_pos - u_img.width//2, 160 - u_img.height//2), u_img)\n",
        "        except: pass\n",
        "\n",
        "    return img\n",
        "\n",
        "# --- Dynamic Calculation Engine (Base 25,000 | Emoji +2,000 | Logo +5,000) ---\n",
